	private static void displaySQL(Map<Long, Quotation> map) {
		for(Long sku : map.keySet()){
			Quotation quo = map.get(sku);
			System.out.println(String.format("insert into kc_quotation(sku, quantity, price, totalprice, period, createdate) values(%d , %d , %.8f , 0 , '2015-11-20', now());", quo.getSku(), quo.getRealQuantity(), quo.getPrice()));
		}
	}

	private static void display(Map<Long, Quotation> map) {
		System.out.println(map.size());
		for(Long sku : map.keySet()){
			Quotation quo = map.get(sku);
			System.out.println(String.format("%d  %d  %.8f", quo.getSku(), quo.getRealQuantity(), quo.getPrice()));
		}
	}

	private static void calc(List<Quotation> list, Map<Long, Quotation> map) {
		for(Quotation q: list){
			Quotation quotation = map.get(q.getSku());
			
			if(quotation == null){
				map.put(q.getSku(), q);
			}
			else{
				quotation.setRealQuantity(q.getRealQuantity() + quotation.getRealQuantity());
				quotation.setQuantity(q.getQuantity()+ quotation.getQuantity());
				quotation.setTotalPrice(q.getTotalPrice().add(quotation.getTotalPrice()));
			}
		}
		
		
		for(Long sku : map.keySet()){
			Quotation quo = map.get(sku);
			quo.setPrice(quo.getTotalPrice().divide(
					new BigDecimal(quo.getQuantity()), 8, RoundingMode.HALF_EVEN));
		}
	}

	private static void init1(List<Quotation> list) {
		for(Quotation quo: list){
			Integer realQuantity = quo.getRealQuantity();
			if(realQuantity.equals(0)){
				quo.setQuantity(1);
			}
			else{
				quo.setQuantity(realQuantity);
			}
			
			quo.setTotalPrice(quo.getPrice().multiply(new BigDecimal(quo.getQuantity())));
		}
	}

